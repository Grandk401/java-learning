package itheima.ShuZu;

public class Demo2Arr {
    public static void main(String[] args) {
        int[][] arr = new int[3][3];
        for (int i = 0; i < arr.length; i++) {
            for (int i1 = 0; i1 < arr.length; i1++) {
                System.out.println(arr[i][i1]);
            }
        }
    }
}
